    private void jChangePasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jChangePasswordMouseClicked
        // TODO add your handling code here:
        String username=jUsername.getText();
        String old_password=jOldPassword.getText();
        String new_password=jNewPassword.getText();
        String conf_new_password=jConfNewPassword.getText();
        
        Connection con = null;
        PreparedStatement st=null;
        ResultSet rs = null;
        con = Link.getConnection();
        String sql="select * from admin where id=? and username=? and password=?";
        try{
        st=con.prepareStatement(sql);
        st.setString(1,id);
        st.setString(2,username);
        st.setString(3,old_password);
        rs=st.executeQuery();
        if(rs.next()){
            if(new_password.equals(conf_new_password)){
                Connection con1 = null;
                PreparedStatement st1=null;
                con1 = Link.getConnection();
                String sql1="update admin set password=? where id='"+id+"'";
                try{
                    st1=con1.prepareStatement(sql1);
                    st1.setString(1,new_password);
                    st1.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Update Successfully");
                    
                    jUsername.setText("");
                    jOldPassword.setText("");
                    jNewPassword.setText("");
                    jConfNewPassword.setText("");
                    }
                catch(Exception e){
                    e.printStackTrace(); 
                }
            }else{
                JOptionPane.showMessageDialog(null,"New Password and Confirmation of New Password are not equal", 
                               "WARNING", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Username or Old Password is incorrect", 
                               "WARNING", JOptionPane.WARNING_MESSAGE);
        }
        }
        catch(Exception e){
           e.printStackTrace(); 
        }
    }//GEN-LAST:event_jChangePasswordMouseClicked

